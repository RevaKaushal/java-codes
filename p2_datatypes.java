//***data types,literals***

public class p2_datatypes {
    public static void main(String[] args){
        System.out.println("diff DATA TYPES are: ");
        byte age= 18;          //byte,int,short,long -->integer PRIMITIVE DATA TYPE  
        int age2 =22;
        short age3=19;
        long agedino=133333L;  //long,float -->compulsion l and f 
        float f1= 3.6f;        //double is default for decimals  
        double d1=3.66;
        boolean b=true;  
        char a= 'A';
        String str= "Reva";
    }
    
}
//byte,long ,float etc--> DATA TYPES
//b,f1,age2,a,d1 etc  --> VARIABLES
//22, 3.6f,133333L etc--> LITERALS  (constant value assigned to variables )
//reserved words      --> KEYWORDS  (look at oracle.com)